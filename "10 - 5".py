#a - b = c / 10 - 5 = 5 => a = b + c / 10 = 5 + 5
def Dota():
    a = 10
    b = 5
    
    левая_граница = -b
    правая_граница = a
    
    while левая_граница <= правая_граница:
        c = (левая_граница + правая_граница) // 2
        
        if b + c == a:
            return c
        elif b + c < a:
            левая_граница = c + 1
        else:
            правая_граница = c - 1
    
    return левая_граница

print(Dota())
